package computerAssemble;

public class CPU {
	int cpuRam;
	double cpuPrice;
	
	public int getCpuRam() {
		return cpuRam;
	}
	public void setCpuRam(int cpuRam) {
		this.cpuRam = cpuRam;
	}
	public double getCpuPrice() {
		return cpuPrice;
	}
	public void setCpuPrice(double cpuPrice) {
		this.cpuPrice = cpuPrice;
	}
	
}
