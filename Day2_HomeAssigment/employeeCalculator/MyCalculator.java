package employeeCalculator;

public class MyCalculator {
	public int getAdd(int number1,int number2)
	{
		return number1+number2;
	}
	public int getSubtract(int number1,int number2)
	{
		return number1-number2;
	}
	public int getMultiply(int number1,int number2)
	{
		return number1*number2;
	}
	public int getDivide(int number1,int number2)
	{
		return number1/number2;
	}
	

}
